def validate_patch(patch):
    missing=sorted((THOUGHT_KEYS|QUESTION_KEYS)-patch.keys())
    if missing: raise ValueError("Не хватает обязательных ключей: "+", ".join(missing))
    changed=sorted(REGISTER_KEYS&patch.keys()); fast={"F1","F2","F3"}; medium={"M1","M2","M3"}
