        if not match: raise ValueError(f"Не удалось разобрать строку: {line!r}")
        key=match.group(1).upper(); value=match.group(2).strip()
        if key not in INDEX: raise ValueError(f"Неизвестный ключ: {key}")
        if key in parsed: raise ValueError(f"Ключ {key} указан дважды.")
